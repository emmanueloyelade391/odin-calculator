function add(x, y) {
  return x + y;
}

function subtract(x, y) {
  return x - y;
}

function multiply(x, y) {
  return x * y;
}

function divide(x, y) {
  return x / y;
}

console.log("Addition:" + add(5, 9));
console.log("Subtraction:" + subtract(5, 9));
console.log("Multiplication:" + multiply(5, 9));
console.log("Division:" + divide(20, 5));